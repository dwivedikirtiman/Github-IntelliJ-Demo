package DataTypeVariableAndConvention;

public class DataTypeVariable {
    public static void main(String[] args) {
        int Myvar1=5;
        System.out.println(Myvar1);
        int myVar2;
        myVar2= 10;
        System.out.println(myVar2);
        char A='g';
        System.out.println(A);
        boolean isAge=  true;
        System.out.println(isAge);
        float myFloat= 5;
        System.out.println(myFloat);
        float myFloat2= 5.0f;
        System.out.println(myFloat2);
        double myDouble1= 4.444d;
        System.out.println(myDouble1);

        String name= "Kirtiman Dwivedi";
        System.out.println(name);
    }
}
